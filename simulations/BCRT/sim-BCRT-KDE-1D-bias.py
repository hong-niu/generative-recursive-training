"""
Recursive density estimation under real–synthetic mixing (biased-real stream; KDE/ECDF option).

This script runs a recursive estimator over T outer iterations. At each iteration n:

  (1) Draw m1 NEW real samples from a *biased* distribution P_n.
      - The unbiased target distribution P0 is a 2-component Gaussian mixture.
      - The biased real stream P_n adds an extra (3rd) Gaussian component with
        time-decaying mixing weight bias_n.

  (2) Draw m2 synthetic samples from the *current* estimator and append them to
      the synthetic pool (memory).

  (3) Recompute the estimator from the accumulated dataset:
          X_train = (all past real) ∪ (all past synthetic).

Estimator modes:
  • KDE mode (default): Gaussian kernel density estimate on a fixed grid with
    bandwidth schedule h_n = h0 * n^(-p_target/2).
    Synthetic samples are generated by: pick a random training point and add
    N(0, h_n^2) noise (i.e., sampling from the KDE).

  • ECDF mode (optional): empirical CDF on the grid, numerically differentiated
    to produce a density for density-based metrics; synthetic samples are drawn
    by resampling from the current empirical distribution (no kernel noise).

Metrics (evaluated against the unbiased target P0 on a fixed grid):
  • Wasserstein-1 distance (W1): computed as ∫ |F_true(x) - F_hat(x)| dx
    using trapezoidal integration on the grid.
  • MMD^2 (Gaussian kernel): computed via grid quadrature using a precomputed
    kernel matrix K over the grid points.

Notes for publication:
  • The “bias” affects ONLY the real-sample stream (the data you observe).
    All metrics are computed w.r.t. the unbiased target P0.
  • Experimental parameters (T, m1, alpha grid, bias-rate grid, bandwidth schedule,
    logging/plotting cadence, etc.) are unchanged from your provided code.
  • Total variation (TV) has been removed entirely (computation, storage, plots,
    slopes, and summaries).

Outputs:
  • Per-condition density plot for the first replicate (final iteration).
  • Trend plots (log10 metric vs log10 M_n) for W1 and MMD^2 on the last replicate.
  • summary_table.csv: theoretical rate vs observed W1 rate (mean/median over reps).
  • all_results.json: raw arrays and per-replicate slopes (W1, MMD^2).

Windows/Jupyter safety:
  • Uses a timestamp without ':' for folder names.
  • No multiprocessing; deterministic structure is unchanged.
"""

