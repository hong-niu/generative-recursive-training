def estimate_power_law_slope(M, vals, drop_first=10):
    """
    Fit log(vals) ≈ a + b log(M) on the tail (drop_first ... end).
    Returns (b, a). In plots we often report observed rate as -b.
    """
    M = np.asarray(M)
    vals = np.asarray(vals)
    if drop_first >= len(M) - 5:
        drop_first = max(0, len(M) - 5)
    Mu = M[drop_first:]
    Vu = vals[drop_first:]
    reg = LinearRegression().fit(np.log(Mu).reshape(-1, 1), np.log(Vu))
    return reg.coef_[0], reg.intercept_


def estimate_power_law_with_loglog(M, vals, drop_first=10):
    """
    Optional extended model: log(vals) ≈ a + b log(M) + c log log(M).
    (Not used in the main loop; kept for parity with your code.)
    """
    M = np.asarray(M)
    vals = np.asarray(vals)
    if drop_first >= len(M) - 5:
        drop_first = max(0, len(M) - 5)
    Mu = M[drop_first:]
    Vu = vals[drop_first:]
    logM = np.log(Mu)
    mask = (Vu > 0) & (Mu > 1.0) & (logM > 0)
    if mask.sum() < 3:
        raise ValueError("Not enough valid points to fit log-log-log model.")
    Mu, Vu, logM = Mu[mask], Vu[mask], logM[mask]
    X = np.column_stack([logM, np.log(logM)])
    reg = LinearRegression().fit(X, np.log(Vu))
    b, c = reg.coef_
    a = reg.intercept_
    return b, c, a, reg


def _make_json_serializable(obj):
    import numpy as _np
    if isinstance(obj, _np.ndarray):
        return obj.tolist()
    if isinstance(obj, (_np.float32, _np.float64)):
        return float(obj)
    if isinstance(obj, (_np.int32, _np.int64)):
        return int(obj)
    return obj


