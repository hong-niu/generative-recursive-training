def _tokenize_texts(tokenizer, texts: List[str]) -> List[int]:
    out = tokenizer(
        texts,
        add_special_tokens=False,
        return_attention_mask=False,
        return_token_type_ids=False,
    )
    flat: List[int] = []
    for seq in out["input_ids"]:
        flat.extend(seq)
        flat.append(tokenizer.eos_token_id)  # doc boundary
    return flat


def build_blocks_from_split(ds_split, tokenizer, block_size: int, text_field: str = "text") -> np.ndarray:
    texts = [t for t in ds_split[text_field] if isinstance(t, str) and t.strip() != ""]
    token_ids = _tokenize_texts(tokenizer, texts)
    n_tokens = len(token_ids)
    n_blocks = n_tokens // block_size
    token_ids = token_ids[: n_blocks * block_size]
    arr = np.asarray(token_ids, dtype=np.int64).reshape(n_blocks, block_size)
    return arr


# -----------------------------
# Synthetic generation (+ timing)
# -----------------------------
def _summarize_times(arr: np.ndarray) -> Dict[str, float]:
    arr = np.asarray(arr, dtype=np.float64)
    if arr.size == 0:
        return {"mean": float("nan"), "median": float("nan"), "p95": float("nan"), "min": float("nan"), "max": float("nan")}
    return {
        "mean": float(np.mean(arr)),
        "median": float(np.median(arr)),
        "p95": float(np.quantile(arr, 0.95)),
        "min": float(np.min(arr)),
        "max": float(np.max(arr)),
    }


@torch.no_grad()
def generate_synthetic_blocks(
    model: AutoModelForCausalLM,
    tokenizer,
    n_blocks: int,
    block_size: int,
    device: torch.device,
    seed: int,
    top_p: float,
    temperature: float,
    repetition_penalty: float,
    micro_gen_bs: int,
    tqdm_file,
) -> Tuple[np.ndarray, Dict[str, Any]]:
    model.eval()
    set_all_seeds(seed)

    bos_id = tokenizer.eos_token_id
    assert bos_id is not None

    blocks = np.empty((n_blocks, block_size), dtype=np.int64)
    per_block_sec = np.empty((n_blocks,), dtype=np.float64)

    filled = 0
    t_start = time.perf_counter()

    pbar = tqdm(total=n_blocks, desc="Generating synthetic blocks", leave=False, file=tqdm_file)
    while filled < n_blocks:
        bs = min(micro_gen_bs, n_blocks - filled)
        prompt = torch.full((bs, 1), bos_id, dtype=torch.long, device=device)
        attn = torch.ones_like(prompt)

        t0 = time.perf_counter()
        gen = model.generate(
            input_ids=prompt,
            max_new_tokens=block_size - 1,
            attention_mask=attn,
            do_sample=True,
            top_p=top_p,
            temperature=temperature,
            repetition_penalty=repetition_penalty,
            eos_token_id=None,  # prevent early stop
            pad_token_id=tokenizer.pad_token_id,
        )
        t1 = time.perf_counter()

        gen = gen[:, :block_size]
        if gen.shape[1] < block_size:
            pad = torch.full(
                (bs, block_size - gen.shape[1]),
                tokenizer.eos_token_id,
                device=device,
                dtype=torch.long,
            )
            gen = torch.cat([gen, pad], dim=1)

        blocks[filled : filled + bs] = gen.detach().cpu().numpy().astype(np.int64, copy=False)

        batch_sec = (t1 - t0)
        per_block_sec[filled : filled + bs] = batch_sec / bs

        filled += bs
        pbar.update(bs)

    pbar.close()
    t_end = time.perf_counter()

    total_sec = t_end - t_start
    timing = {
        "n_blocks": int(n_blocks),
        "block_size": int(block_size),
        "micro_gen_bs": int(micro_gen_bs),
        "total_gen_sec": float(total_sec),
        "sec_per_block_summary": _summarize_times(per_block_sec),
        "sec_per_block": per_block_sec,
        "approx_tokens_generated": int(n_blocks * block_size),
        "approx_tokens_per_sec": float((n_blocks * block_size) / total_sec) if total_sec > 0 else float("nan"),
        "approx_blocks_per_sec": float(n_blocks / total_sec) if total_sec > 0 else float("nan"),
    }
    return blocks, timing


