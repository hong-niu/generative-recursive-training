#!/usr/bin/env python3
"""
Recursive real-synthetic training experiment for GPT-2 language modeling.

This script studies recursive training of an autoregressive language model under
controlled real-synthetic data mixing. At each outer iteration, the current
model is trained for a fixed number of full passes over a newly constructed
token-block dataset D_t. The dataset contains a specified mixture of disjoint
real text blocks from WikiText and synthetic text blocks generated by the model
from the previous iteration.

Training protocol:

    - Text is tokenized into fixed-length blocks of length block_size.
    - Real blocks are sampled without replacement across outer iterations.
    - At each iteration t, the training set D_t has fixed size m_total_blocks.
    - If first_iter_all_real=True, then D_1 consists entirely of real blocks.
    - For t >= 2, unless otherwise specified, D_t contains:

        - m_real = round(alpha_real * m_total_blocks) real blocks
        - m_synth = m_total_blocks - m_real synthetic blocks
        - The model is trained for exactly epochs_per_iter full passes over D_t.
        - This fixed-epoch design ensures that all blocks introduced at a given 
            iteration receive the same number of training passes.

Synthetic-only option:

    - If train_synth_only_after_first=True, then for all t >= 2:
        - m_real = 0
        - m_synth = m_total_blocks
        - In this setting, the first iteration remains unchanged when
            first_iter_all_real=True, so the model is initialized on real data before
            recursive synthetic-only training begins.
        - This option is useful for studying collapse or degradation under fully
            synthetic recursive training.

Model and optimization:
    - The base model is GPT-2, loaded through Hugging Face Transformers.


Evaluation:

- Evaluation is performed on a fixed held-out WikiText test set.
- At each outer iteration, the script records: test perplexity, 
    predictive entropy in nats and bits, qualitative generated samples, 
    distinct-1, distinct-2, and distinct-3 diversity metrics

Outputs:

    - The script saves run configuration, logs, per-iteration metadata, evaluation
        history, generated samples, optional synthetic blocks, synthetic generation
        timing summaries, and optionally model checkpoints after each iteration.
    - These outputs are intended to support reproducibility and downstream
        re-analysis without rerunning the full recursive experiment.
    
    - Text samples can be generated from saved models (separate script) 

Dependencies:
    - pip install torch transformers datasets tqdm numpy
"""


