@torch.no_grad()
def evaluate_ppl_and_entropy(model, dataloader, device, fp16: bool, tqdm_file) -> Dict[str, float]:
    model.eval()

    losses = []
    ent_sum = 0.0
    tok_count = 0

    use_cuda_amp = (fp16 and device.type == "cuda")
    autocast = torch.cuda.amp.autocast if use_cuda_amp else torch.cpu.amp.autocast

    for batch in tqdm(dataloader, desc="Eval", leave=False, file=tqdm_file):
        input_ids = batch["input_ids"].to(device, non_blocking=True)
        labels = batch["labels"].to(device, non_blocking=True)

        with autocast():
            out = model(input_ids=input_ids, labels=labels)
            loss = out.loss
            logits = out.logits

        losses.append(loss.detach().float().cpu().item())

        shift_logits = logits[:, :-1, :].float()
        shift_labels = labels[:, 1:].contiguous()
        mask = (shift_labels != -100)

        logp = torch.log_softmax(shift_logits, dim=-1)
        p = torch.exp(logp)
        ent = -(p * logp).sum(dim=-1)

        ent_sum += ent[mask].sum().detach().cpu().item()
        tok_count += int(mask.sum().detach().cpu().item())

    mean_loss = float(np.mean(losses)) if losses else float("nan")
    test_ppl = float(math.exp(mean_loss)) if math.isfinite(mean_loss) else float("nan")

    entropy_nats = (ent_sum / tok_count) if tok_count > 0 else float("nan")
    entropy_bits = entropy_nats / math.log(2) if math.isfinite(entropy_nats) else float("nan")

    return {"test_ppl": test_ppl, "entropy_nats": entropy_nats, "entropy_bits": entropy_bits}


def distinct_n_from_token_ids(token_ids: List[int], n: int) -> float:
    if n <= 0:
        raise ValueError("n must be >= 1")
    if len(token_ids) < n:
        return float("nan")
    ngrams = [tuple(token_ids[i : i + n]) for i in range(len(token_ids) - n + 1)]
    if not ngrams:
        return float("nan")
    return len(set(ngrams)) / len(ngrams)


@torch.no_grad()
def sample_and_distinct(
    model,
    tokenizer,
    device,
    prompt: str,
    n_samples: int,
    max_new_tokens: int,
    temperature: float,
    top_p: float,
    repetition_penalty: float,
) -> Dict[str, Any]:
    model.eval()
    inputs = tokenizer(prompt, return_tensors="pt").to(device)

    input_ids = inputs["input_ids"].repeat(n_samples, 1)
    attn_mask = inputs.get("attention_mask", torch.ones_like(inputs["input_ids"])).repeat(n_samples, 1)

    out = model.generate(
        input_ids=input_ids,
        attention_mask=attn_mask,
        max_new_tokens=max_new_tokens,
        do_sample=True,
        top_p=top_p,
        temperature=temperature,
        repetition_penalty=repetition_penalty,
        pad_token_id=tokenizer.eos_token_id,
    )

    prompt_len = input_ids.shape[1]
    cont_ids = out[:, prompt_len:].detach().cpu().tolist()

    d1, d2, d3 = [], [], []
    samples_text = []
    samples_full_text = []
    for ids in cont_ids:
        d1.append(distinct_n_from_token_ids(ids, 1))
        d2.append(distinct_n_from_token_ids(ids, 2))
        d3.append(distinct_n_from_token_ids(ids, 3))
        cont_txt = tokenizer.decode(ids, skip_special_tokens=True)
        samples_text.append(cont_txt)
        samples_full_text.append(prompt + cont_txt)

    def nanmean(vals: List[float]) -> float:
        vals = [v for v in vals if isinstance(v, float) and math.isfinite(v)]
        return float(np.mean(vals)) if vals else float("nan")

    return {
        "samples_text": samples_text,
        "samples_full_text": samples_full_text,
        "distinct_1": nanmean(d1),
        "distinct_2": nanmean(d2),
        "distinct_3": nanmean(d3),
    }


def save_samples(iter_dir: Path, prompt: str, gen_stats: Dict[str, Any]) -> None:
    samples_text = gen_stats["samples_text"]

    txt_path = iter_dir / "samples.txt"
    with open(txt_path, "w", encoding="utf-8") as f:
        f.write(f"PROMPT:\n{prompt}\n")
        f.write("\n" + "=" * 80 + "\n")
        for i, s in enumerate(samples_text, 1):
            f.write(f"\n--- SAMPLE {i:03d} (continuation only) ---\n")
            f.write(s)
            f.write("\n")
        f.write("\n" + "=" * 80 + "\n")

    json_path = iter_dir / "samples.json"
    payload = {
        "prompt": prompt,
        "distinct_1": float(gen_stats["distinct_1"]),
        "distinct_2": float(gen_stats["distinct_2"]),
        "distinct_3": float(gen_stats["distinct_3"]),
        "samples_continuation": gen_stats["samples_text"],
        "samples_full": gen_stats["samples_full_text"],
    }
    with open(json_path, "w", encoding="utf-8") as f:
        json.dump(payload, f, indent=2, ensure_ascii=False)


def save_synth_timing(iter_dir: Path, t: int, timing: Dict[str, Any], cfg: Config) -> Dict[str, str]:
    per_block = timing.pop("sec_per_block")
    timing_json = dict(timing)
    timing_json["iter"] = int(t)

    json_path = iter_dir / f"synth_timing_iter{t:03d}.json"
    with open(json_path, "w", encoding="utf-8") as f:
        json.dump(timing_json, f, indent=2)

    npy_path = None
    if cfg.save_synth_time_per_block and cfg.save_synth_time_per_block_npy:
        npy_path = iter_dir / f"synth_time_per_block_iter{t:03d}.npy"
        np.save(npy_path, per_block)

    summary_path = iter_dir / f"synth_timing_iter{t:03d}.txt"
    with open(summary_path, "w", encoding="utf-8") as f:
        f.write(f"iter={t}\n")
        f.write(f"total_gen_sec={timing_json['total_gen_sec']:.6f}\n")
        f.write(f"approx_blocks_per_sec={timing_json['approx_blocks_per_sec']:.6f}\n")
        f.write(f"approx_tokens_per_sec={timing_json['approx_tokens_per_sec']:.6f}\n")
        s = timing_json["sec_per_block_summary"]
        f.write("sec_per_block_summary:\n")
        for k in ["mean", "median", "p95", "min", "max"]:
            f.write(f"  {k}: {s[k]:.6f}\n")

    out = {"synth_timing_json": str(json_path.resolve()), "synth_timing_txt": str(summary_path.resolve())}
    if npy_path is not None:
        out["synth_time_per_block_npy"] = str(npy_path.resolve())
    return out


