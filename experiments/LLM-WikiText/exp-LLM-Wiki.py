@dataclass
class Config:
    # Data
    dataset_name: str = "wikitext"
    dataset_config: str = "wikitext-103-raw-v1"
    block_size: int = 1024  # fixed-length token blocks
    max_test_blocks: int = 2000  # cap for faster eval; set None to use all

    # Recursion
    outer_iters: int = 50
    alpha_real: float = 0.5
    m_total_blocks: int = 1024  # blocks per iteration dataset D_t
    epochs_per_iter: int = 1
    first_iter_all_real: bool = True  # D_1 is all real

    # NEW: collapse test switch (NEED FOR alpha=0.0 otherwise still have some REAL DATA)
    train_synth_only_after_first: bool = False # if True: for t>=2, m_real=0, m_synth=m_total

    # Model
    model_name: str = "gpt2"  # 124M
    fp16: bool = True

    # Optimization
    lr: float = 2e-5
    weight_decay: float = 0.01
    adam_beta1: float = 0.9
    adam_beta2: float = 0.95
    adam_eps: float = 1e-8
    grad_clip: float = 1.0

    # Batch / accumulation (tuned for 16GB VRAM + seq 1024)
    micro_batch_size: int = 1
    grad_accum_steps: int = 32
    num_workers: int = 2

    # Generation (synthetic)
    gen_top_p: float = 0.95
    gen_temperature: float = 1.0
    gen_repetition_penalty: float = 1.0
    gen_seed_offset: int = 10_000
    micro_gen_bs: int = 8  # generation batch size

    # Qualitative sampling + distinct-n
    sample_prompt: str = "The history of science"
    sample_n: int = 8
    sample_max_new_tokens: int = 300

    # Repro / logging
    seed: int = 1234
    log_every_steps: int = 50

    # Output
    out_dir: str = "runs/llm_recursive_mix"
    save_model_each_iter: bool = True
    save_synth_each_iter: bool = True

    # Logging
    log_filename: str = "run.log"  # saved under out_dir

    # Synthetic timing output
    save_synth_time_per_block: bool = True
    save_synth_time_per_block_npy: bool = True


def set_all_seeds(seed: int):
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    torch.cuda.manual_seed_all(seed)


def iter_sizes(t: int, cfg: Config) -> Tuple[int, int]:
    """
    Returns (m_real, m_synth) for outer iteration t (1-indexed).

    Behavior:
      - If first_iter_all_real and t==1: all-real, no synth.
      - Else if train_synth_only_after_first and t>=2: synthetic-only.
      - Else: mixed according to alpha_real.
    """
    if cfg.first_iter_all_real and t == 1:
        return cfg.m_total_blocks, 0

    if cfg.train_synth_only_after_first and t >= 2:
        return 0, cfg.m_total_blocks

    m_real = int(round(cfg.alpha_real * cfg.m_total_blocks))
    m_real = max(1, min(cfg.m_total_blocks, m_real))
    return m_real, cfg.m_total_blocks - m_real


