# Training
# -----------------------------
def train_one_iteration_full_passes(
    model,
    train_dataset: Dataset,
    device: torch.device,
    cfg: Config,
    tqdm_file,
) -> dict:
    model.train()
    train_loader = DataLoader(
        train_dataset,
        batch_size=cfg.micro_batch_size,
        shuffle=True,
        num_workers=cfg.num_workers,
        pin_memory=(device.type == "cuda"),
        drop_last=False,
        collate_fn=collate_batch,
    )

    no_decay = ["bias", "ln_", "LayerNorm.weight"]
    params = []
    for n, p in model.named_parameters():
        if not p.requires_grad:
            continue
        wd = 0.0 if any(nd in n for nd in no_decay) else cfg.weight_decay
        params.append({"params": [p], "weight_decay": wd})

    opt = torch.optim.AdamW(
        params,
        lr=cfg.lr,
        betas=(cfg.adam_beta1, cfg.adam_beta2),
        eps=cfg.adam_eps,
    )

    steps_per_epoch = math.ceil(len(train_loader) / cfg.grad_accum_steps)
    total_steps = cfg.epochs_per_iter * steps_per_epoch
    warmup = max(10, int(0.03 * total_steps))
    sched = get_linear_schedule_with_warmup(opt, num_warmup_steps=warmup, num_training_steps=total_steps)

    scaler = torch.cuda.amp.GradScaler(enabled=(cfg.fp16 and device.type == "cuda"))
    autocast = torch.cuda.amp.autocast if (cfg.fp16 and device.type == "cuda") else torch.cpu.amp.autocast

    global_step = 0
    running = 0.0
    t0 = time.perf_counter()

    for epoch in range(cfg.epochs_per_iter):
        pbar = tqdm(train_loader, desc=f"Train epoch {epoch+1}/{cfg.epochs_per_iter}", leave=False, file=tqdm_file)
        opt.zero_grad(set_to_none=True)

        for step, batch in enumerate(pbar):
            input_ids = batch["input_ids"].to(device, non_blocking=True)
            labels = batch["labels"].to(device, non_blocking=True)

            with autocast():
                out = model(input_ids=input_ids, labels=labels)
                loss = out.loss / cfg.grad_accum_steps

            scaler.scale(loss).backward()
            running += loss.detach().float().cpu().item()

            if (step + 1) % cfg.grad_accum_steps == 0:
                if cfg.grad_clip and cfg.grad_clip > 0:
                    scaler.unscale_(opt)
                    torch.nn.utils.clip_grad_norm_(model.parameters(), cfg.grad_clip)

                scaler.step(opt)
                scaler.update()
                opt.zero_grad(set_to_none=True)
                sched.step()
                global_step += 1

                if cfg.log_every_steps and global_step % cfg.log_every_steps == 0:
                    avg = running / cfg.log_every_steps
                    running = 0.0
                    lr_now = sched.get_last_lr()[0]
                    pbar.set_postfix(loss=f"{avg:.4f}", lr=f"{lr_now:.2e}")

    elapsed = time.perf_counter() - t0
    return {"train_steps": global_step, "train_time_sec": float(elapsed)}


# -----------------------------
# Main recursion
# -----------------------------
def main():
    cfg = Config()
    out_dir = Path(cfg.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    tqdm_stream = sys.stderr  # original stderr (before tee)

    log_path = setup_run_logging(out_dir, cfg.log_filename)
    logger = logging.getLogger("recursive_train")

    with open(out_dir / "config.json", "w", encoding="utf-8") as f:
        json.dump(asdict(cfg), f, indent=2)

    logger.info("Run dir: %s", str(out_dir.resolve()))
    logger.info("Log file: %s", str(log_path.resolve()))
    logger.info("train_synth_only_after_first: %s", cfg.train_synth_only_after_first)

    set_all_seeds(cfg.seed)
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    logger.info("Device: %s", str(device))

    run_t0 = time.perf_counter()

    tokenizer = AutoTokenizer.from_pretrained(cfg.model_name, use_fast=True)
    if tokenizer.pad_token is None:
        tokenizer.add_special_tokens({"pad_token": "<|pad|>"})
    tokenizer.padding_side = "left"

    model = AutoModelForCausalLM.from_pretrained(cfg.model_name)
    model.resize_token_embeddings(len(tokenizer))
    model.config.pad_token_id = tokenizer.pad_token_id
    model.to(device)

    logger.info("Loading dataset %s/%s", cfg.dataset_name, cfg.dataset_config)
    ds = load_dataset(cfg.dataset_name, cfg.dataset_config)

    logger.info("Tokenizing + blocking train split (real pool)...")
    real_blocks = build_blocks_from_split(ds["train"], tokenizer, cfg.block_size)
    logger.info("Real train blocks: %s (block_size=%d)", f"{real_blocks.shape[0]:,}", cfg.block_size)

    logger.info("Tokenizing + blocking test split (fixed real test)...")
    test_blocks = build_blocks_from_split(ds["test"], tokenizer, cfg.block_size)
    if cfg.max_test_blocks is not None:
        test_blocks = test_blocks[: cfg.max_test_blocks]
    logger.info("Test blocks used: %s", f"{test_blocks.shape[0]:,}")

    test_ds = TokenBlockDataset(test_blocks)
    test_loader = DataLoader(
        test_ds,
        batch_size=max(1, cfg.micro_batch_size),
        shuffle=False,
        num_workers=cfg.num_workers,
        pin_memory=(device.type == "cuda"),
        drop_last=False,
        collate_fn=collate_batch,
    )

    # Ensure enough real blocks ONLY for iterations that actually use real blocks.
    needed_real = 0
    for t in range(1, cfg.outer_iters + 1):
        m_real_t, _ = iter_sizes(t, cfg)
        needed_real += m_real_t

    if needed_real > real_blocks.shape[0]:
        raise RuntimeError(
            f"Not enough real blocks for the chosen schedule: need {needed_real}, have {real_blocks.shape[0]}.\n"
            f"Either reduce outer_iters/m_total_blocks or adjust flags."
        )

    # Partition real blocks into disjoint shards for iterations needing real.
    # We still build a list shards[t-1], but for iterations with m_real_t==0 we store an empty array.
    shards: List[np.ndarray] = []
    cursor = 0
    for t in range(1, cfg.outer_iters + 1):
        m_real_t, _ = iter_sizes(t, cfg)
        if m_real_t > 0:
            shards.append(real_blocks[cursor : cursor + m_real_t])
            cursor += m_real_t
        else:
            shards.append(np.empty((0, cfg.block_size), dtype=np.int64))

    synth_prev: Optional[np.ndarray] = None

    hist_path = out_dir / "history.jsonl"
    history: List[Dict[str, Any]] = []

    # Baseline eval
    eval_t0 = time.perf_counter()
    base_eval = evaluate_ppl_and_entropy(model, test_loader, device, cfg.fp16, tqdm_file=tqdm_stream)
    eval_t1 = time.perf_counter()
    logger.info(
        "Iteration 0 (base model): test_ppl=%.3f | H=%.3f nats (%.3f bits) | eval_time=%.2fs",
        base_eval["test_ppl"], base_eval["entropy_nats"], base_eval["entropy_bits"], (eval_t1 - eval_t0)
    )
    with open(hist_path, "a", encoding="utf-8") as f:
        f.write(json.dumps({"iter": 0, **base_eval, "eval_time_sec": float(eval_t1 - eval_t0)}) + "\n")

    for t in range(1, cfg.outer_iters + 1):
        iter_t0 = time.perf_counter()
        print("\n" + "=" * 80)
        logger.info("Outer iteration %d/%d", t, cfg.outer_iters)

        m_real_t, m_synth_t = iter_sizes(t, cfg)
        logger.info("Iter %d: real=%d, synth=%d, alpha≈%.4f", t, m_real_t, m_synth_t, (m_real_t / cfg.m_total_blocks) if cfg.m_total_blocks else float("nan"))

        R_t = shards[t - 1]

        if m_synth_t > 0:
            if synth_prev is None:
                raise RuntimeError(
                    f"Iteration {t} expects synthetic data (m_synth={m_synth_t}) but synth_prev is None. "
                    f"Ensure iteration 1 produces synthetic for iteration 2."
                )
            S_prev = synth_prev
            if S_prev.shape[0] != m_synth_t:
                raise RuntimeError(
                    f"Iteration {t} expects {m_synth_t} synthetic blocks but got {S_prev.shape[0]}."
                )
        else:
            S_prev = np.empty((0, cfg.block_size), dtype=np.int64)

        D_t = np.concatenate([R_t, S_prev], axis=0)
        assert D_t.shape[0] == cfg.m_total_blocks, (D_t.shape[0], cfg.m_total_blocks)

        iter_dir = out_dir / f"iter_{t:03d}"
        iter_dir.mkdir(parents=True, exist_ok=True)

        meta = {
            "iter": t,
            "m_total_blocks": int(cfg.m_total_blocks),
            "m_real": int(m_real_t),
            "m_synth": int(m_synth_t),
            "alpha_used": float(m_real_t / cfg.m_total_blocks) if cfg.m_total_blocks else float("nan"),
        }
        with open(iter_dir / "meta.json", "w", encoding="utf-8") as f:
            json.dump(meta, f, indent=2)

        # Train
        train_t0 = time.perf_counter()
        train_ds = TokenBlockDataset(D_t)
        train_stats = train_one_iteration_full_passes(model, train_ds, device, cfg, tqdm_file=tqdm_stream)
        train_t1 = time.perf_counter()

        # Eval
        eval_t0 = time.perf_counter()
        eval_stats = evaluate_ppl_and_entropy(model, test_loader, device, cfg.fp16, tqdm_file=tqdm_stream)
        eval_t1 = time.perf_counter()

        # Sampling (qualitative)
        samp_t0 = time.perf_counter()
        gen_stats = sample_and_distinct(
            model=model,
            tokenizer=tokenizer,
            device=device,
            prompt=cfg.sample_prompt,
            n_samples=cfg.sample_n,
            max_new_tokens=cfg.sample_max_new_tokens,
            temperature=cfg.gen_temperature,
            top_p=cfg.gen_top_p,
            repetition_penalty=cfg.gen_repetition_penalty,
        )
        samp_t1 = time.perf_counter()

        save_samples(iter_dir, cfg.sample_prompt, gen_stats)

        logger.info(
            "After iter %d: test_ppl=%.3f | H=%.3f nats (%.3f bits) | train_steps=%d",
            t, eval_stats["test_ppl"], eval_stats["entropy_nats"], eval_stats["entropy_bits"], train_stats["train_steps"]
        )
        logger.info(
            "Distinct: d1=%.4f d2=%.4f d3=%.4f | sample_time=%.2fs",
            gen_stats["distinct_1"], gen_stats["distinct_2"], gen_stats["distinct_3"], (samp_t1 - samp_t0)
        )

        record: Dict[str, Any] = {
            **meta,
            **train_stats,
            **eval_stats,
            "train_wall_sec": float(train_t1 - train_t0),
            "eval_wall_sec": float(eval_t1 - eval_t0),
            "sample_wall_sec": float(samp_t1 - samp_t0),
            "distinct_1": float(gen_stats["distinct_1"]),
            "distinct_2": float(gen_stats["distinct_2"]),
            "distinct_3": float(gen_stats["distinct_3"]),
            "samples_path_txt": str((iter_dir / "samples.txt").resolve()),
            "samples_path_json": str((iter_dir / "samples.json").resolve()),
        }

        # Generate S_t for NEXT iter
        synth_gen_paths: Dict[str, str] = {}
        if t < cfg.outer_iters:
            _, m_synth_next = iter_sizes(t + 1, cfg)
            if m_synth_next > 0:
                logger.info("Generating S_%d (size=%d) for iteration %d...", t, m_synth_next, t + 1)
                synth_t0 = time.perf_counter()
                synth_prev, synth_timing = generate_synthetic_blocks(
                    model=model,
                    tokenizer=tokenizer,
                    n_blocks=m_synth_next,
                    block_size=cfg.block_size,
                    device=device,
                    seed=cfg.seed + cfg.gen_seed_offset + 1_000 * t,
                    top_p=cfg.gen_top_p,
                    temperature=cfg.gen_temperature,
                    repetition_penalty=cfg.gen_repetition_penalty,
                    micro_gen_bs=cfg.micro_gen_bs,
                    tqdm_file=tqdm_stream,
                )
                synth_t1 = time.perf_counter()

                if cfg.save_synth_each_iter:
                    np.save(iter_dir / f"synth_blocks_iter{t:03d}.npy", synth_prev)

                synth_gen_paths = save_synth_timing(iter_dir, t, synth_timing, cfg)

                logger.info(
                    "Synthetic gen time (wall): %.2fs | total_gen_sec=%.2fs | blocks/sec=%.4f",
                    (synth_t1 - synth_t0),
                    synth_timing["total_gen_sec"],
                    synth_timing["approx_blocks_per_sec"],
                )
            else:
                synth_prev = None

        iter_t1 = time.perf_counter()
        record["iter_wall_sec"] = float(iter_t1 - iter_t0)
        record.update(synth_gen_paths)

        history.append(record)
        with open(hist_path, "a", encoding="utf-8") as f:
            f.write(json.dumps(record) + "\n")

        if cfg.save_model_each_iter:
            model.save_pretrained(iter_dir / "model")
            tokenizer.save_pretrained(iter_dir / "model")

        logger.info("Iter %d wall time: %.2fs", t, record["iter_wall_sec"])

    with open(out_dir / "history_full.json", "w", encoding="utf-8") as f:
        json.dump(history, f, indent=2)

    run_t1 = time.perf_counter()
    total_wall = run_t1 - run_t0
    logger.info("Done.")
    logger.info("Total wall time: %.2fs (%.2f min, %.2f hr)", total_wall, total_wall / 60.0, total_wall / 3600.0)
    logger.info("Logs: %s", str(hist_path.resolve()))
    logger.info("Run dir: %s", str(out_dir.resolve()))


if __name__ == "__main__":
    main()