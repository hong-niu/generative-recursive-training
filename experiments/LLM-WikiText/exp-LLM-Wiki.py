class TeeStream:
    """Write to both console stream and a file stream."""
    def __init__(self, console_stream, file_stream):
        self.console_stream = console_stream
        self.file_stream = file_stream

    def write(self, data):
        self.console_stream.write(data)
        self.file_stream.write(data)

    def flush(self):
        self.console_stream.flush()
        self.file_stream.flush()


def setup_run_logging(out_dir: Path, log_filename: str) -> Path:
    out_dir.mkdir(parents=True, exist_ok=True)
    log_path = out_dir / log_filename

    f = open(log_path, "a", buffering=1, encoding="utf-8")
    sys.stdout = TeeStream(sys.__stdout__, f)
    sys.stderr = TeeStream(sys.__stderr__, f)

    logging.basicConfig(
        level=logging.INFO,
        format="%(asctime)s | %(levelname)s | %(message)s",
        handlers=[logging.StreamHandler(sys.stdout)],
    )
    return log_path


