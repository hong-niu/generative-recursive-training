class TokenBlockDataset(Dataset):
    """Dataset of fixed-length token blocks stored as a 2D numpy array: [N, block_size]."""

    def __init__(self, blocks_np: np.ndarray):
        assert blocks_np.ndim == 2, "blocks_np must be [N, block_size]"
        self.blocks = blocks_np.astype(np.int64, copy=False)

    def __len__(self):
        return self.blocks.shape[0]

    def __getitem__(self, idx):
        x = torch.from_numpy(self.blocks[idx])
        return {"input_ids": x, "labels": x.clone()}


def collate_batch(features):
    input_ids = torch.stack([f["input_ids"] for f in features], dim=0)
    labels = torch.stack([f["labels"] for f in features], dim=0)
    return {"input_ids": input_ids, "labels": labels}


