# UNet-ish denoiser
# ============================================================
class SinusoidalTimeEmbedding(nn.Module):
    def __init__(self, dim: int):
        super().__init__()
        self.dim = dim

    def forward(self, t: torch.Tensor) -> torch.Tensor:
        half = self.dim // 2
        device = t.device
        freqs = torch.exp(
            -np.log(10000.0) * torch.arange(half, device=device).float() / max(half - 1, 1)
        )
        args = t.float().unsqueeze(1) * freqs.unsqueeze(0)
        emb = torch.cat([torch.sin(args), torch.cos(args)], dim=1)
        if self.dim % 2 == 1:
            emb = torch.cat([emb, torch.zeros((emb.size(0), 1), device=device)], dim=1)
        return emb


class ResBlock(nn.Module):
    def __init__(self, ch: int, tdim: int):
        super().__init__()
        self.norm1 = nn.GroupNorm(8, ch)
        self.conv1 = nn.Conv2d(ch, ch, 3, padding=1)
        self.norm2 = nn.GroupNorm(8, ch)
        self.conv2 = nn.Conv2d(ch, ch, 3, padding=1)
        self.time = nn.Linear(tdim, ch)

    def forward(self, x, t_emb):
        h = self.conv1(F.silu(self.norm1(x)))
        h = h + self.time(F.silu(t_emb)).unsqueeze(-1).unsqueeze(-1)
        h = self.conv2(F.silu(self.norm2(h)))
        return x + h


class Down(nn.Module):
    def __init__(self, ch: int):
        super().__init__()
        self.conv = nn.Conv2d(ch, ch, 4, stride=2, padding=1)

    def forward(self, x):
        return self.conv(x)


class Up(nn.Module):
    def __init__(self, ch: int):
        super().__init__()
        self.conv = nn.ConvTranspose2d(ch, ch, 4, stride=2, padding=1)

    def forward(self, x):
        return self.conv(x)
    
# add attention 
class AttentionBlock(nn.Module):
    def __init__(self, ch: int):
        super().__init__()
        self.norm = nn.GroupNorm(8, ch)
        self.q = nn.Conv2d(ch, ch, 1)
        self.k = nn.Conv2d(ch, ch, 1)
        self.v = nn.Conv2d(ch, ch, 1)
        self.proj_out = nn.Conv2d(ch, ch, 1)

        # THE FIX: Zero-initialize the final projection layer
        # This makes the entire block act as an Identity function at initialization.
        nn.init.zeros_(self.proj_out.weight)
        nn.init.zeros_(self.proj_out.bias)

    def forward(self, x):
        B, C, H, W = x.shape
        h = self.norm(x)

        # Flatten spatial dimensions for attention
        q = self.q(h).view(B, C, -1).transpose(1, 2)  
        k = self.k(h).view(B, C, -1).transpose(1, 2)  
        v = self.v(h).view(B, C, -1).transpose(1, 2)  

        # Use PyTorch's optimized attention (much faster than manual bmm)
        out = F.scaled_dot_product_attention(q, k, v) 

        # Reshape back to image dimensions
        out = out.transpose(1, 2).contiguous().view(B, C, H, W)

        # Skip connection: initially x + 0
        return x + self.proj_out(out)
    
class SimpleUNet_1xAttn(nn.Module):
    def __init__(self, base_ch=64, time_dim=128, in_ch=1):
        super().__init__()
        self.t_embed = SinusoidalTimeEmbedding(time_dim)
        self.t_mlp = nn.Sequential(
            nn.Linear(time_dim, time_dim),
            nn.SiLU(),
            nn.Linear(time_dim, time_dim),
        )

        ch1 = base_ch
        ch2 = base_ch * 2

        self.in_conv = nn.Conv2d(in_ch, ch1, 3, padding=1)

        self.rb1 = ResBlock(ch1, time_dim)
        self.down1 = Down(ch1)

        self.rb2 = ResBlock(ch1, time_dim)
        self.to_ch2 = nn.Conv2d(ch1, ch2, 1)
        self.down2 = Down(ch2)

        # --- MIDDLE BLOCKS ---
        self.rb_mid1 = ResBlock(ch2, time_dim)
        
        # 1. ADD THE ATTENTION BLOCK HERE
        self.attn_mid = AttentionBlock(ch2) 
        
        self.rb_mid2 = ResBlock(ch2, time_dim)
        self.rb_mid3 = ResBlock(ch2, time_dim)  

        # --- UP BLOCKS ---
        self.up2 = Up(ch2)
        self.from_ch2 = nn.Conv2d(ch2, ch1, 1)
        self.rb3 = ResBlock(ch1, time_dim)

        self.up1 = Up(ch1)
        self.rb4 = ResBlock(ch1, time_dim)

        self.out_conv = nn.Conv2d(ch1, in_ch, 3, padding=1)

    def forward(self, x, t):
        t_emb = self.t_mlp(self.t_embed(t))

        x1 = self.in_conv(x)      
        x1 = self.rb1(x1, t_emb)
        d1 = self.down1(x1)       

        d1 = self.rb2(d1, t_emb)
        skip14 = d1               

        d1 = self.to_ch2(d1)      
        d2 = self.down2(d1)       

        # --- FORWARD PASS THRU MIDDLE ---
        mid = self.rb_mid1(d2, t_emb)
        
        # 2. CALL THE ATTENTION BLOCK HERE
        mid = self.attn_mid(mid) 
        
        mid = self.rb_mid2(mid, t_emb)
        mid = self.rb_mid3(mid, t_emb)  

        u2 = self.up2(mid)        
        u2 = self.from_ch2(u2)    
        u2 = u2 + skip14
        u2 = self.rb3(u2, t_emb)

        u1 = self.up1(u2)         
        u1 = u1 + x1
        u1 = self.rb4(u1, t_emb)

        return self.out_conv(u1)
    
