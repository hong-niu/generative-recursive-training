        d1 = self.down1(x1)       # (B, ch1, 14, 14)

        d1 = self.rb2(d1, t_emb)
        skip14 = d1               # (B, ch1, 14, 14)

        d1 = self.to_ch2(d1)      # (B, ch2, 14, 14)
