def per_step_mix_counts(alpha_real: float, total_per_step: int, tstep: int) -> tuple[int, int]:
    """
    First-step override:
      t = 1  -> always all real, regardless of alpha_real

    Thereafter:
      t >= 2 ->
        m1 = round(alpha_real * total_per_step)
        m2 = total_per_step - m1

    In particular:
      alpha_real == 0.0 gives
        t = 1  -> all real
        t >= 2 -> all synthetic
    """
    if tstep == 1:
        return total_per_step, 0

    m1 = int(round(alpha_real * total_per_step))
    m2 = total_per_step - m1
    return m1, m2




def build_balanced_class_index_lists(targets, rng: np.random.Generator, n_classes: int = 10) -> Dict[int, List[int]]:
    targets_np = np.asarray(targets)
    class_to_indices: Dict[int, List[int]] = {}
    for cls in range(n_classes):
        idx = np.flatnonzero(targets_np == cls).astype(int)
        idx = idx.copy()
        rng.shuffle(idx)
        class_to_indices[cls] = idx.tolist()
    return class_to_indices


def sample_real_indices_balanced(
    m1: int,
    class_to_indices: Dict[int, List[int]],
    class_ptrs: Dict[int, int],
    used_real: set,
    rng: np.random.Generator,
    exhausted_state: Dict[str, bool],
    tstep: int,
) -> List[int]:
    if m1 <= 0:
        return []

    n_classes = len(class_to_indices)
    base = m1 // n_classes
    remainder = m1 % n_classes
    class_order = list(range(n_classes))
    if remainder > 0:
        extra = rng.permutation(n_classes)[:remainder].tolist()
    else:
        extra = []
    target_counts = {cls: base + (1 if cls in extra else 0) for cls in class_order}

    picked: List[int] = []

    for cls in class_order:
        need = target_counts[cls]
        pool = class_to_indices[cls]
        ptr = class_ptrs[cls]

        while need > 0 and ptr < len(pool):
            idx = int(pool[ptr])
            ptr += 1
            if idx not in used_real:
                used_real.add(idx)
                picked.append(idx)
                need -= 1

        class_ptrs[cls] = ptr

        if need > 0:
            if not exhausted_state.get("warned", False):
                print(
                    f"[warn] Exhausted balanced unique MNIST train indices at t={tstep}. "
                    f"Filling remaining class-balanced real samples with replacement.",
                    flush=True,
                )
                exhausted_state["warned"] = True
            replace_pool = class_to_indices[cls]
            if len(replace_pool) == 0:
                raise ValueError(f"No MNIST training examples found for class {cls}")
            picked.extend(rng.choice(replace_pool, size=need, replace=True).astype(int).tolist())

    rng.shuffle(picked)
    return picked


def sample_real_indices_unbalanced(
    m1: int,
    all_idx: np.ndarray,
    ptr_state: Dict[str, int],
    N_train: int,
    used_real: set,
    rng: np.random.Generator,
    exhausted_state: Dict[str, bool],
    tstep: int,
) -> List[int]:
    picked: List[int] = []
    ptr = ptr_state["ptr"]

    while len(picked) < m1 and ptr < N_train:
        idx = int(all_idx[ptr])
        ptr += 1
        if idx not in used_real:
            used_real.add(idx)
            picked.append(idx)

    ptr_state["ptr"] = ptr

    if len(picked) < m1:
        if not exhausted_state.get("warned", False):
            print(
                f"[warn] Exhausted unique MNIST train indices at t={tstep}. "
                f"Filling remaining real samples with replacement.",
                flush=True,
            )
            exhausted_state["warned"] = True
        need = m1 - len(picked)
        picked.extend(rng.choice(N_train, size=need, replace=True).astype(int).tolist())

    return picked

