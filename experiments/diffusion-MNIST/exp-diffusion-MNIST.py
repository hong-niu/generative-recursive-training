# FID helpers (pixel-space Fréchet distance)
# ============================================================
@torch.no_grad()
def extract_fid_features(x: torch.Tensor) -> torch.Tensor:
    """
    x: (N,1,28,28), values in [-1,1] or [0,1]
    returns: (N, 784) flattened pixel features
    """
    return x.view(x.size(0), -1).to(dtype=torch.float64)


def compute_mean_and_cov(features: torch.Tensor):
    """
    features: (N, D), torch tensor
    returns:
      mu: numpy array (D,)
      sigma: numpy array (D, D)
    """
    x = features.detach().cpu().numpy()
    mu = np.mean(x, axis=0)
    sigma = np.cov(x, rowvar=False)
    return mu, sigma


def symmetric_matrix_sqrt(mat: np.ndarray, eps: float = 1e-12) -> np.ndarray:
    """
    Matrix square root for a symmetric PSD matrix using eigh.
    """
    mat = 0.5 * (mat + mat.T)
    vals, vecs = np.linalg.eigh(mat)
    vals = np.clip(vals, a_min=0.0, a_max=None)
    root = (vecs * np.sqrt(vals + eps)) @ vecs.T
    return 0.5 * (root + root.T)


def frechet_distance_from_stats(
    mu1: np.ndarray,
    sigma1: np.ndarray,
    mu2: np.ndarray,
    sigma2: np.ndarray,
    eps: float = 1e-6,
) -> float:
    """
    Stable Fréchet distance between two Gaussians:
      ||mu1 - mu2||^2 + Tr(sigma1 + sigma2 - 2 * (sigma1^{1/2} sigma2 sigma1^{1/2})^{1/2})

    Uses symmetric PSD square roots to avoid scipy.
    """
    mu1 = np.atleast_1d(mu1).astype(np.float64)
    mu2 = np.atleast_1d(mu2).astype(np.float64)
    sigma1 = np.atleast_2d(sigma1).astype(np.float64)
    sigma2 = np.atleast_2d(sigma2).astype(np.float64)

    d = sigma1.shape[0]
    eye = np.eye(d, dtype=np.float64)

    sigma1 = 0.5 * (sigma1 + sigma1.T) + eps * eye
    sigma2 = 0.5 * (sigma2 + sigma2.T) + eps * eye

    diff = mu1 - mu2
    diff_term = diff @ diff

    sqrt_sigma1 = symmetric_matrix_sqrt(sigma1, eps=eps)
    inner = sqrt_sigma1 @ sigma2 @ sqrt_sigma1
    inner = 0.5 * (inner + inner.T)
    covmean = symmetric_matrix_sqrt(inner, eps=eps)

    fid = diff_term + np.trace(sigma1) + np.trace(sigma2) - 2.0 * np.trace(covmean)
    return float(np.real(fid))


@torch.no_grad()
def frechet_distance_between_batches(real_x: torch.Tensor, gen_x: torch.Tensor) -> float:
    """
    Compute FID-style Fréchet distance on flattened pixel features.
    """
    real_feat = extract_fid_features(real_x)
    gen_feat = extract_fid_features(gen_x)

    mu_r, sig_r = compute_mean_and_cov(real_feat)
    mu_g, sig_g = compute_mean_and_cov(gen_feat)

    return frechet_distance_from_stats(mu_r, sig_r, mu_g, sig_g)


