def experiment_root_dir(cfg: ArgsConfig) -> Path:
    regime_bits = []
    regime_bits.append("train=accum" if cfg.train_on_accumulated else "train=fresh")
    if cfg.reinit_each_step:
        regime_bits.append("reinit=1")
    if cfg.balance_real_classes:
        regime_bits.append("balreal=1")
    regime_suffix = "_" + "_".join(regime_bits) if regime_bits else ""

    base_dir = Path(cfg.output_root) if (cfg.output_root and cfg.output_root.strip()) else Path("logs")

    return (
        base_dir
        / f"mnist_4grid_fixed_FID_seed={cfg.base_seed}"
          f"_T={cfg.T_steps}"
          f"_total={cfg.total_per_step}"
          f"_epochs={cfg.epochs_per_step}"
          f"_reps={cfg.n_reps}"
          f"_Tdiff={cfg.Tdiff}_{cfg.schedule}"
          f"_bs={cfg.batch_size}"
          f"_lr={cfg.lr:g}"
          f"_ch={cfg.base_ch}"
          f"{regime_suffix}"
    )


