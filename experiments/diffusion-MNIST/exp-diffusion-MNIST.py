#!/usr/bin/env python3
"""
Recursive real-synthetic training experiment on MNIST using a DDPM epsilon-prediction model.

This script evaluates a recursive training procedure in which, at each outer
iteration, the model is trained on a newly constructed batch containing a fixed
mixture of real MNIST training images and synthetic images generated by the
current model. The experiment is designed to match the fresh-batch theoretical
setting: unless --train-on-accumulated is specified, there is no replay buffer,
no accumulated synthetic pool, and no accumulated real-data pool. Each newly
introduced datum is used only at the iteration in which it is added and receives
exactly epochs_per_step full training passes.

Data-generation protocol:

    - At t = 1, the training batch is all real MNIST data, regardless of alpha_real.
    - For t >= 2, each batch contains:
    - m1 = round(alpha_real * total_per_step) new real MNIST training images
    - m2 = total_per_step - m1 synthetic images generated by the current model

    - Thus, alpha_real controls the fraction of real data introduced after the
    first iteration. For example, alpha_real = 0 uses all real data at t = 1 and
    all synthetic data thereafter.

Model and training:

    - The generative model is a DDPM trained by epsilon prediction.
    - The default denoiser is a small UNet-style architecture with attention.
    - Training uses a cosine or linear diffusion noise schedule.
    - Optional flags allow accumulated-data training, model reinitialization at
    each outer step, and approximately class-balanced real MNIST sampling.

Evaluation:

    - Evaluation is performed against a fixed, held-out, class-balanced subset of
    the MNIST test set.
    - Model quality is measured using a Fréchet-style distance computed in flattened
    MNIST pixel space.
    - The script records FID trajectories, training losses, fitted power-law slopes,
    run configurations, sample grids, and optional per-iteration snapshot grids.


For each snapshot step, the script saves:
    - the newly constructed training batch without highlighting
    - the same training batch with synthetic samples outlined in purple
    - a model-generated sample grid after training at that step, when t > 1

Dependencies:
    - pip install torch torchvision numpy matplotlib scikit-learn
"""


