# Recursive training loop (core): fresh-batch-only
# ============================================================
def run_recursive_diffusion_mnist_freshbatch(
    mnist_train_ds,
    mnist_test_ds,
    T=200,
    total_per_step=200,
    alpha_real=0.5,
    epochs_per_step=1,
    lr=2e-4,
    batch_size=128,
    eval_true=1024,
    eval_gen=1024,
    device="cuda" if torch.cuda.is_available() else "cpu",
    seed=1234,
    Tdiff=200,
    schedule_kind="cosine",
    beta_start=1e-4,
    beta_end=2e-2,
    base_ch=64,
    # snapshotting
    snapshot_interval=0,
    snapshot_n=64,
    snapshots_dir: Optional[Path] = None,
    run_seed: int = 0,
    rep: int = 0,
    train_on_accumulated: bool = False,
    reinit_each_step: bool = False,
    balance_real_classes: bool = False,
    log_fn=None,
):
    seed_everything(seed)

    rng = np.random.default_rng(seed)
    rng_eval = np.random.default_rng(seed + 101)

    tgen = torch.Generator(device=device)
    tgen.manual_seed(seed + 202)

    def init_model_and_opt(step_seed: Optional[int] = None):
        if step_seed is not None:
            torch.manual_seed(int(step_seed))
            if torch.cuda.is_available():
                torch.cuda.manual_seed(int(step_seed))
                torch.cuda.manual_seed_all(int(step_seed))
        model_local = SimpleUNet(base_ch=base_ch, time_dim=128, in_ch=1).to(device)
        opt_local = torch.optim.AdamW(model_local.parameters(), lr=lr)
        return model_local, opt_local

    model, opt = init_model_and_opt()

    betas, alphas, alpha_bars = make_beta_schedule(
        Tdiff=Tdiff, beta_start=beta_start, beta_end=beta_end, device=device, kind=schedule_kind
    )

    # Unique real pool over time (for selecting NEW real TRAIN points)
    N_train = len(mnist_train_ds)
    train_targets = np.asarray(mnist_train_ds.targets)
    all_idx = np.arange(N_train, dtype=int)
    rng.shuffle(all_idx)
    ptr_state = {"ptr": 0}
    exhausted_state = {"warned": False}
    used_real = set()

    class_to_indices = build_balanced_class_index_lists(train_targets, rng, n_classes=10)
    class_ptrs = {cls: 0 for cls in class_to_indices}

    accum_batches: List[torch.Tensor] = []

    M_arr = np.zeros(T, dtype=int)
    fid_history = np.zeros(T, dtype=float)
    loss_history = np.zeros(T, dtype=float)

    # Fixed real_eval from MNIST TEST set (held-out, no training leakage)
    # Make it class-balanced across digits 0..9 as much as possible.
    N_test = len(mnist_test_ds)
    n_eval_true = min(eval_true, N_test)

    # MNIST dataset stores labels in .targets
    test_targets = np.asarray(mnist_test_ds.targets)

    n_classes = 10
    per_class = n_eval_true // n_classes
    remainder = n_eval_true % n_classes

    eval_idx = []

    for cls in range(n_classes):
        cls_idx = np.flatnonzero(test_targets == cls)

        # distribute remainder across first few classes
        take = per_class + (1 if cls < remainder else 0)

        if take > len(cls_idx):
            raise ValueError(
                f"Not enough test examples for class {cls}: requested {take}, available {len(cls_idx)}"
            )

        chosen = rng_eval.choice(cls_idx, size=take, replace=False)
        eval_idx.extend(chosen.tolist())

    # shuffle final balanced eval set so classes are mixed
    rng_eval.shuffle(eval_idx)

    real_eval = torch.stack([mnist_test_ds[i][0] for i in eval_idx], dim=0)

    def maybe_snapshot(step_batch_cpu: torch.Tensor, is_synth: torch.Tensor, tstep: int):
        do_snap = (
            snapshots_dir is not None
            and int(snapshot_interval) > 0
            and (tstep == 1 or tstep == T or (tstep % int(snapshot_interval) == 0))
        )
        if not do_snap:
            return False

        snap_dir = Path(snapshots_dir)
        snap_dir.mkdir(parents=True, exist_ok=True)
        tag = f"alpha{alpha_pretty(alpha_real)}_rep{rep:03d}_t={tstep:04d}_seed={run_seed}"

        # save NEW training batch snapshots (after shuffle): plain + highlighted
        perm = rng.permutation(step_batch_cpu.size(0))
        perm_t = torch.from_numpy(perm).long()

        x_show = step_batch_cpu[perm_t]
        is_synth_show = is_synth[perm_t]

        # Plain (no highlight)
        save_grid_from_tensor_batch(
            x_show,
            snap_dir / f"{tag}_train_plain.png",
            n=snapshot_n,
            is_synth=None,
        )

        # Highlighted (synthetic outlined)
        save_grid_from_tensor_batch(
            x_show,
            snap_dir / f"{tag}_train_highlight.png",
            n=snapshot_n,
            is_synth=is_synth_show,
        )

        del x_show, is_synth_show

        # save model gen snapshot after training (only if tstep > 1)
        if tstep > 1:
            model.eval()
            with torch.no_grad():
                snap_gen = torch.Generator(device=device)
                snap_gen.manual_seed(int(seed + 700000 + tstep))  # stable per-step seed
                xg_snap = ddpm_sample(
                    model=model,
                    n=int(snapshot_n),
                    betas=betas,
                    alphas=alphas,
                    alpha_bars=alpha_bars,
                    device=device,
                    tgen=snap_gen,
                    shape=(1, 28, 28),
                ).cpu()
            save_grid_from_tensor_batch(xg_snap, snap_dir / f"{tag}_gen.png", n=snapshot_n)
            del xg_snap

        return True

    epochs = max(1, int(epochs_per_step))

    for tstep in range(1, T + 1):
        m1, m2 = per_step_mix_counts(alpha_real, total_per_step, tstep)

        # ----------------------------------------------------
        # 1) Build NEW batch: m1 new real + m2 new synth
        # ----------------------------------------------------
        picked = []
        if m1 > 0:
            if balance_real_classes:
                picked = sample_real_indices_balanced(
                    m1=m1,
                    class_to_indices=class_to_indices,
                    class_ptrs=class_ptrs,
                    used_real=used_real,
                    rng=rng,
                    exhausted_state=exhausted_state,
                    tstep=tstep,
                )
            else:
                picked = sample_real_indices_unbalanced(
                    m1=m1,
                    all_idx=all_idx,
                    ptr_state=ptr_state,
                    N_train=N_train,
                    used_real=used_real,
                    rng=rng,
                    exhausted_state=exhausted_state,
                    tstep=tstep,
                )

        real_new = None
        if len(picked) > 0:
            real_new = torch.stack([mnist_train_ds[i][0] for i in picked], dim=0)  # CPU [-1,1]

        synth_new = None
        if tstep > 1 and m2 > 0:
            model.eval()
            with torch.no_grad():
                synth_new = ddpm_sample(
                    model=model,
                    n=m2,
                    betas=betas,
                    alphas=alphas,
                    alpha_bars=alpha_bars,
                    device=device,
                    tgen=tgen,
                    shape=(1, 28, 28),
                ).cpu()

        # Concatenate + build mask (real=False, synth=True)
        parts = []
        is_synth_list: List[bool] = []

        if real_new is not None:
            parts.append(real_new)
            is_synth_list.extend([False] * int(real_new.size(0)))

        if synth_new is not None:
            parts.append(synth_new)
            is_synth_list.extend([True] * int(synth_new.size(0)))

        if len(parts) == 0:
            M_arr[tstep - 1] = 0
            loss_history[tstep - 1] = np.nan
            fid_history[tstep - 1] = np.nan
            continue

        step_batch = torch.cat(parts, dim=0)  # CPU
        is_synth = torch.tensor(is_synth_list, dtype=torch.bool)

        added = int(step_batch.size(0))  # usually = total_per_step
        prev = int(M_arr[tstep - 2]) if tstep > 1 else 0
        M_arr[tstep - 1] = prev + added

        # Optional snapshots (train batch: plain + highlight; plus gen after training)
        _ = maybe_snapshot(step_batch, is_synth, tstep)

        # ----------------------------------------------------
        # 2) Train on chosen regime for `epochs` full passes
        # ----------------------------------------------------
        accum_batches.append(step_batch.clone())
        train_batch = torch.cat(accum_batches, dim=0) if train_on_accumulated else step_batch

        if reinit_each_step:
            model, opt = init_model_and_opt(step_seed=seed + 500000 + tstep)

        model.train()
        running = 0.0
        n_steps = 0

        ds_train = TensorDataset(train_batch)
        loader = DataLoader(
            ds_train,
            batch_size=batch_size,
            shuffle=True,
            drop_last=False,
            pin_memory=(device == "cuda"),
        )

        for _ep in range(epochs):
            for (x0_cpu,) in loader:
                x0 = x0_cpu.to(device=device, dtype=torch.float32, non_blocking=True)

                loss = diffusion_loss_eps(model, x0, alpha_bars, tgen)
                opt.zero_grad(set_to_none=True)
                loss.backward()
                nn.utils.clip_grad_norm_(model.parameters(), 1.0)
                opt.step()

                running += float(loss.item())
                n_steps += 1
                del x0, loss

        loss_history[tstep - 1] = running / max(n_steps, 1)

        # ----------------------------------------------------
        # 3) Eval (FID against fixed MNIST TEST subset)
        # ----------------------------------------------------
        model.eval()
        with torch.no_grad():
            g_eval = ddpm_sample(
                model=model,
                n=eval_gen,
                betas=betas,
                alphas=alphas,
                alpha_bars=alpha_bars,
                device=device,
                tgen=tgen,
                shape=(1, 28, 28),
            ).cpu()

        n_eval = min(real_eval.size(0), g_eval.size(0))
        fid = frechet_distance_between_batches(real_eval[:n_eval], g_eval[:n_eval])
        fid_history[tstep - 1] = fid

        train_size = int(train_batch.size(0))
        msg = (
            f"[t={tstep:4d}] m1={m1:4d} | m2={m2:4d} | "
            f"M_cum={M_arr[tstep-1]:7d} | M_new={added:5d} | train_N={train_size:6d} | "
            f"FID={fid_history[tstep-1]:.6f} | train_loss={loss_history[tstep-1]:.6f} | "
            f"epochs={epochs}"
        )
        if log_fn is not None:
            log_fn(msg)
        else:
            print(msg, flush=True)

        del g_eval, train_batch, step_batch, real_new, synth_new, parts, is_synth
        gc.collect()
        if device == "cuda":
            torch.cuda.empty_cache()

    return {
        "M": M_arr,
        "fid_history": fid_history,
        "loss_history": loss_history,
        "denoiser": model,
        "betas": betas,
        "alphas": alphas,
        "alpha_bars": alpha_bars,
        "seed": seed,
    }


