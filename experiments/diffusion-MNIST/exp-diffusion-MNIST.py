def diffusion_loss_eps_original(model, x0, alpha_bars, tgen: torch.Generator):
    B = x0.size(0)
    Tdiff = alpha_bars.numel()

    t = torch.randint(0, Tdiff, (B,), device=x0.device, generator=tgen)
    eps = torch.randn(x0.shape, device=x0.device, dtype=x0.dtype, generator=tgen)

    abar = alpha_bars[t].view(B, 1, 1, 1)
    x_t = torch.sqrt(abar) * x0 + torch.sqrt(1.0 - abar) * eps

    eps_hat = model(x_t, t)
    return torch.mean((eps - eps_hat) ** 2)

# time weight the loss, MIN-SNR weighting 
def diffusion_loss_eps(model, x0, alpha_bars, tgen: torch.Generator, gamma=5.0):
    B = x0.size(0)
    Tdiff = alpha_bars.numel()

    t = torch.randint(0, Tdiff, (B,), device=x0.device, generator=tgen)
    eps = torch.randn(x0.shape, device=x0.device, dtype=x0.dtype, generator=tgen)

    abar = alpha_bars[t].view(B, 1, 1, 1)
    x_t = torch.sqrt(abar) * x0 + torch.sqrt(1.0 - abar) * eps

    eps_hat = model(x_t, t)
    
    # --- Min-SNR Weighting ---
    # SNR = alpha_bar / (1 - alpha_bar)
    # Adding a tiny epsilon to the denominator to prevent division by zero
    snr = abar / (1.0 - abar + 1e-8) 
    
    # For epsilon prediction, the Min-SNR weight is min(1.0, gamma / SNR)
    loss_weight = torch.clamp(gamma / snr, max=1.0)
    
    # Calculate unreduced MSE, apply weight, then mean
    raw_loss = (eps - eps_hat) ** 2
    weighted_loss = loss_weight * raw_loss
    
    return torch.mean(weighted_loss)

@torch.no_grad()
def ddpm_sample(model, n, betas, alphas, alpha_bars, device, tgen: torch.Generator, shape=(1, 28, 28)):
    """
    Standard DDPM sampling with posterior variance beta_tilde.
    """
    Tdiff = betas.numel()
    x = torch.randn(n, *shape, device=device, generator=tgen)

    for ti in reversed(range(Tdiff)):
        t = torch.full((n,), ti, device=device, dtype=torch.long)
        eps_hat = model(x, t)

        beta_t = betas[ti]
        alpha_t = alphas[ti]
        abar_t = alpha_bars[ti]
        abar_prev = alpha_bars[ti - 1] if ti > 0 else torch.tensor(1.0, device=device, dtype=alpha_bars.dtype)

        mean = (1.0 / torch.sqrt(alpha_t)) * (x - (beta_t / torch.sqrt(1.0 - abar_t)) * eps_hat)

        if ti > 0:
            beta_tilde = beta_t * (1.0 - abar_prev) / (1.0 - abar_t)
            beta_tilde = beta_tilde.clamp(min=1e-20)
            z = torch.randn(x.shape, device=x.device, dtype=x.dtype, generator=tgen)
            x = mean + torch.sqrt(beta_tilde) * z
        else:
            x = mean

    return x.clamp(-1, 1)


