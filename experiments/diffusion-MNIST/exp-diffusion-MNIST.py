def main():
    cfg = cfg_from_args(build_argparser().parse_args())

    root, figs_root, data_root = ensure_experiment_dirs(cfg)

    # save experiment-level config
    save_json(asdict(cfg), root / "config_experiment.json")

    print(f"Device: {cfg.device}")
    print(f"Experiment root: {root}")
    print(f"Figures root:    {figs_root}")
    print(f"Data root:       {data_root}")
    if cfg.alpha_real is None:
        print(f"Mode: SWEEP (alphas={cfg.alphas_real}, n_reps={cfg.n_reps})", flush=True)
    else:
        print(f"Mode: ALPHA-PARALLEL (alpha_real={cfg.alpha_real:.2f}, reps=0..{cfg.n_reps-1})", flush=True)

    print(f"epochs_per_step={cfg.epochs_per_step}", flush=True)
    print(
        f"Training regime: {'accumulated-data' if cfg.train_on_accumulated else 'fresh-batch'} | "
        f"reinit_each_step={cfg.reinit_each_step} | "
        f"balance_real_classes={cfg.balance_real_classes}",
        flush=True,
    )

    if cfg.snapshot_interval > 0:
        print(f"Snapshots: every {cfg.snapshot_interval} steps (and t=1,t=T), snapshot_n={cfg.snapshot_n}", flush=True)
        print("Snapshot training grids saved as: *_train_plain.png and *_train_highlight.png", flush=True)
    else:
        print("Snapshots: disabled (snapshot_interval=0)", flush=True)

    mnist_train_ds = load_mnist_train(data_root)
    mnist_test_ds = load_mnist_test(data_root)

    # Choose which alphas to run
    if cfg.alpha_real is not None:
        alphas = [float(cfg.alpha_real)]
    else:
        alphas = list(cfg.alphas_real)

    # Run (alphas x reps), reps sequential in-process
    for alpha_real in alphas:
        print("\n" + "#" * 70)
        m1_after = int(round(alpha_real * cfg.total_per_step))
        m2_after = cfg.total_per_step - m1_after
        print(
            f"alpha_real={alpha_real:.2f}  "
            f"(t=1: m1={cfg.total_per_step}, m2=0; "
            f"t>=2: m1={m1_after}, m2={m2_after})"
        )
        print("#" * 70, flush=True)

        for rep in range(cfg.n_reps):
            _ = run_one(alpha_real, rep, cfg, mnist_train_ds, mnist_test_ds, root)

    print("\nDone. Per-run outputs are under:")
    print(root / "runs", flush=True)


if __name__ == "__main__":
    main()